    public AnimalCadastro(Animal a) {
        this.animal = a;
        this.repositorio = RepositorioFactory.getAnimalRepo();
        cliRepo = RepositorioFactory.getClienteRepo();
        cliente = new Cliente(); 
        raca = new Raca();
        racaRepo = RepositorioFactory.getRacaRepo();
        initComponents();
        this.setComponentes();
        
        cbxCliente.addItem(cliente);
        List<Cliente> resultado = cliRepo.Buscar(cliente);
        for(Cliente c : resultado){
            
           
            cbxCliente.addItem(c);     
          
        }
        
        cbxRaca.addItem(raca);
        List<Raca> resulta = racaRepo.Buscar(raca);
        for(Raca r : resulta){
            
           
            cbxRaca.addItem(r);     
            
          
        }     
        
    }

